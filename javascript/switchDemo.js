let day = 1;

let dayname ;

switch (day) {
    case 1:
        dayname = "Monday";
        break;
    case 2:
        dayname = "Tuesday";
        break;

    case 3:
        dayname = "Wednesday";
        break;
        
    default:
        dayname = "Invalid option"
        break;
}

console.log(dayname);